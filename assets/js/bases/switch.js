const dia = 5;


switch (dia) {  // switch (variable)
    case 0:  // case (valor)
        console.log('Domingo');
        break;  // break
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miércoles');
        break;
    case 4:
        console.log('Jueves');
        break;
    case 5:
        console.log('Viernes');
        break;
    case 6:
        console.log('Sábado');
        break;
    default:  // default
        console.log('Día no válido');
        break;
}